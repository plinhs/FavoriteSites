    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed

try {
    
    try {
        con = DriverManager.getConnection("jdbc:mysql://localhost/favorite_sites","root","10293847");
    } catch (SQLException ex) {
        Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
    }
    
            String query = "UPDATE visits SET country_name = ?, city_name = ?, year_visited = ?, season_visited = ?, best_feature = ?, comments = ?, rating = ? WHERE visit_id='"+carriedVisitID+"' ";
                 
            ps = con.prepareStatement(query);
           
            ps.setString(1, countrytxt.getText()); // Use the logged-in username
            ps.setString(2, citytxt.getText());
            int yearValue;
            int ratingValue;      
        try {
            yearValue = Integer.parseInt(yeartxt.getText());
            ratingValue = Integer.parseInt(ratingtxt.getText());
        } catch (NumberFormatException ex) {
            
            JOptionPane.showMessageDialog(null, "Please enter valid integer values for year and rating.");
            return; 
        }

        ps.setInt(3, yearValue);
        ps.setString(4, seasontxt.getText());
        ps.setString(5, bestfeaturetxt.getText());
        ps.setString(6, commenttxt.getText());
        ps.setInt(7, ratingValue);
        ps.execute();
        
        JOptionPane.showMessageDialog(null,"Location successfully updated.");
        this.dispose();
        MainFrame m = new MainFrame();
        m.setLoggedInUsername(loggedInUsername); 
        m.startLoadingTable();
        m.setVisible(true);
   
    
} catch (SQLException ex) {
            Logger.getLogger(UpdateFrame.class.getName()).log(Level.SEVERE, null, ex);
        }       

    }//GEN-LAST:event_updateButtonActionPerformed

