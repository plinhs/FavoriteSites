    private void fetchVisitDetails() {
         
          try {
                con = DriverManager.getConnection("jdbc:mysql://localhost/favorite_sites","root","10293847");
            } catch (SQLException ex) {
                Logger.getLogger(UpdateFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        String sql = "SELECT country_name, city_name, year_visited, season_visited, best_feature, comments, rating FROM visits WHERE visit_id='"+carriedVisitID+"' ";
        
        try {
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            
            if (rs.next()) {
                country = rs.getString("country_name");
                city = rs.getString("city_name");
                year = rs.getString("year_visited");
                season = rs.getString("season_visited");
                bestfeature = rs.getString("best_feature");
                comment = rs.getString("comments");
                rating = rs.getString("rating");
                
                countrytxt.setText(country);
                citytxt.setText(city);
                yeartxt.setText(year);
                seasontxt.setText(season);
                bestfeaturetxt.setText(bestfeature);
                commenttxt.setText(comment);
                ratingtxt.setText(rating);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


